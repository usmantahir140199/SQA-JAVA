public static void main(String args[])
{
    Scanner sc = new Scanner(System.in);
    int n = sc.nextInt();
    int m = sc.nextInt();
    for(int i=0;i<n;i++)
    {
        System.out.Println("Hello");  
    }
    for(int j=0;j<m;j++)
    {
        System.out.Println("Hello"); 
    }
}

// Space Complexity : Scanner class object(sc) , i m,n,j : 4 variables and 1 class object